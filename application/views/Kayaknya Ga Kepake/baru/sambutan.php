                    <h2>Sambutan</h2>
                    <p class="lead text-justify" contenteditable="true">&nbsp;
<strong><i>Assalamu’alaikum Wr Wb</i></strong><br>
Pembangunan kesehatan merupakan hal yang sangat penting karena merupakan salah satu unsur dari kesejahteraan masyarakat  yang sangat menentukan ketahanan dari bangsa Indonesia. 
Dalam mewujudkan visi dan misinya Dinas Kesehatan Kabupaten Purworejo telah menempatkan sistem informasi menjadi salah satu strategi utama dalam Sistem Kesehatan. Yaitu dengan Membangun ”Sistem Informasi Kesehatan  Daerah yang terintegrasi ”,  dimana  sistem  informasi  kesehatan  diarahkan  kepada kondisi yang ideal yaitu mampu menyediakan data dan informasi kesehatan  yang evidence based secara cepat untuk  meningkatkan  efisiensi  dan  efektifitas  pengelolaan pembangunan  kesehatan melalui integrasi system. 
Semakin globalnya mobilitas penduduk menyebabkan kebutuhan data dan informasi kesehatan meningkat. Kepedulian masyarakat akan informasi tentang situasi kesehatan terutama terhadap masalah-masalah kesehatan yang berhubungan langsung dengan kesehatan mereka semakin tinggi pula, karena kesehatan menyangkut hajat hidup orang banyak dan semua orang butuh untuk sehat. Selain itu dengan adanya Undang-undang Nomor 14 Tahun 2009 tentang Keterbukaan Informasi maka pihak pengelola program harus bisa menyediakan dan memberikan data dan informasi yang dibutuhkan masyarakat secara baik, informastif dan tepat waktu.
Dalam SKN disebutkan bahwa keberhasilan manajemen kesehatan sangat ditentukan antara lain oleh tersedianya data dan informasi kesehatan, dukungan kemajuan ilmu pengetahuan, dukungan hukum kesehatan serta administrasi kesehatan. Lebih lanjut disebutkan bahwa SKN terdiri dari 6 (enam) subsistem, yaitu 1) Subsistem Upaya Kesehatan, 2)Subsistem Pembiayaan Kesehatan, 3) Subsistem Sumber Daya Manusia Kesehatan, 4) Subsistem Obat dan Perbekalan Kesehatan, 5) Subsistem Pemberdayaan Masyarakat dan 6) Subsistem Manajemen Kesehatan. Untuk manajemen kesehatan tingkat keberhasilannya sangat ditentukan oleh tersedianya data dan informasi dengan dukungan kemajuan ilmu pengetahuan dan teknologi.
Dalam tatanan desentralisasi atau otonomi daerah di bidang kesehatan, kualitas dari informasi kesehatan daerah dengan Sistem Informasi Kesehatan Daerahl (SIKDA) sangat ditentukan dari keberhasilan dalam Penataan kembali dan pengembangan lebih lanjut data dan informasi. 
Hasil dari Pengelolalan SIKDA akan menjadi informasi yang sangat dibutuhkan Publik guna meningkatkan derajat kesehatan  melalui  konten www.dinkes.purworejokab.go.id.
Konten tersebut diharapkan dapat mempermudah masyarakat  untuk  mengakses informasi  public tentang pembangunan kesehatan di Kabupaten Purworejo.
Demikian kami sangat mengharapkan kritik dan saran yang baik guna peningkatan pelayanan kami di bidang kesehatan mendorong partisipasi masyarakat dalam proses pengambilan keputusan, dan mewujudkan penyelenggaraan negara yang baik yaitu yang transparan, efektif, efisien, akuntabel serta dapat dipertanggungjawabkan.<br>
Sekian, terima kasih.<br>

